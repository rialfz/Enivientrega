package Movil.Data;

/**
 *
 * @author ricar
 */
public class Movil {
    private Double identificacion;
    private String nombre;
    private String telefono;
    private String direccion;
    private String codigo;
    private int estado;
    private String refenrenciaNombre;
    private String referenciaTelefono;

    public Movil() {
    }
    
    public Double getIdentificacion() {
        return identificacion;
    }

    public void setIdentificacion(Double identificacion) {
        this.identificacion = identificacion;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getTelefono() {
        return telefono;
    }

    public void setTelefono(String telefono) {
        this.telefono = telefono;
    }

    public String getDireccion() {
        return direccion;
    }

    public void setDireccion(String direccion) {
        this.direccion = direccion;
    }

    public String getCodigo() {
        return codigo;
    }

    public void setCodigo(String codigo) {
        this.codigo = codigo;
    }

    public int getEstado() {
        return estado;
    }

    public void setEstado(int estado) {
        this.estado = estado;
    }

    public String getRefenrenciaNombre() {
        return refenrenciaNombre;
    }

    public void setRefenrenciaNombre(String refenrenciaNombre) {
        this.refenrenciaNombre = refenrenciaNombre;
    }

    public String getReferenciaTelefono() {
        return referenciaTelefono;
    }

    public void setReferenciaTelefono(String referenciaTelefono) {
        this.referenciaTelefono = referenciaTelefono;
    }

  
    
    

}


